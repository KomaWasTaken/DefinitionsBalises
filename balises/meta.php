            <h2>La balise &lt;meta&gt;</h2>

            <p>Les balises <strong>&lt;meta&gt;</strong> (aussi appelées meta-tags) sont des informations incluses à l'intérieur des pages web.
            Ces informations sont placées dans l'en-tête d'une page web à l'intérieur de la balise head. Les balises meta sont relatives à la page et donnent des informations plus généralement aux moteurs de recherche (bien que d'autres applications sont possibles).
            </p>

            <p>Les informations sont très vastes, il peut s'agir du langage de la page, de l'auteur du document, d'une description de la page et de bien d'autres choses encore.
            Parmi les balises les plus importantes et les plus connus dans le monde du référencement il y a :
            </p>
    
            <ul>
                <li>La balise <strong>meta description</strong>. Cette balise n'a aucun impact sur le positionnement d'une page, elle est uniquement utilisée par le moteur de recherche Google pour présenter les résultats. Autrement dit, le texte de la balise meta description peut être utilisé pour présenter votre page sur les SERP.</li>
                <li>La balise <strong>meta keywords</strong>. Cette balise n'a plus d'impact pour le positionnement sur Google, mais sert encore un peu sur Yahoo.</li>
                <li>La balise <strong>meta robots</strong>. Cette balise indique aux moteurs de recherches entre autres s'ils doivent indexer la page, suivre les liens et archiver la page dans le cache de Google. Par défaut, si elle n'est pas utilisée les principaux moteurs de recherche indexent et suivent les liens.</li>
            </ul>

